#!/usr/bin/python3

""" Defines the class Square that defines a square"""


class Square:
    """
    Square class

    Attributes:
    __size (int): size of the side of a square
    """

    def __init__(self, size):
        """
        Initializes the square

        Args:
            size (int): size of the size of a square

        Returns: None
        """
        self.__size = size
        return None
